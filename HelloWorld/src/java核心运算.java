public class java核心运算 {
    public static void main(String[] args) {
        int a = 1;
        int b = 13;
        int c = 10;
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(b%c);
        System.out.println(b/c);
        System.out.println(a+=b);
        System.out.println(a++);
        System.out.println(++a);
    }
}
