public class 变量的定义 {
    //1.定义变量 变量就是程序执行过程中可以发生改变的量
    //2.怎样定义变量：数据类型 变量名=初始化的值
    //3.变量的存储是内存随机分配的一块区域
    public static void main(String[] args) {
        int num=12;
        System.out.println(num);
        System.out.println(num+1);
        char zifu='中';
        System.out.println(zifu);
        System.out.println(zifu+1);
    }
}
